/// Boogy SDK convention: the column name Boogy-authored APIs
/// use to record the owning principal of a row. The platform's
/// resource-level auth helpers (`owns_resource`, `find_owned`,
/// `load_owned`) take the owner-column name as a parameter so APIs
/// CAN choose otherwise, but using this constant — and the column
/// name it carries — keeps multi-tenant ownership uniform across the
/// fleet, makes audit / migration / billing tooling possible, and
/// removes a small but real source of LLM-author drift.
pub const DEFAULT_OWNER_COL: &str = "owner_principal";

/// Minimal request type used by the router.
/// APIs should convert their WIT HttpRequest into this (the `wit_glue!`
/// macro generates the converter automatically).
pub struct Request {
    pub method: String,
    pub path: String,
    pub headers: Vec<(String, String)>,
    pub body: Option<Vec<u8>>,
    pub path_params: Vec<(String, String)>,
    pub query_params: Vec<(String, String)>,
}

/// Returns the path to the WIT directory (for use in build.rs scripts or wit_bindgen paths).
pub fn wit_path() -> &'static str {
    concat!(env!("CARGO_MANIFEST_DIR"), "/../boogy-wit/wit")
}

/// The trait every Boogy API implements.
///
/// Both methods are associated functions — they describe the API as a
/// whole, not an instance. The `wit_glue!` macro generates an `impl Guest`
/// that calls them in the right order: `init_tables` once on every
/// request (idempotent because each table create is idempotent) and
/// `build_router` per request to dispatch to handlers.
///
/// # Example
///
/// ```ignore
/// impl boogy_sdk::Api for MyApi {
///     fn init_tables() {
///         create_table_from(&Table::new("users").text("email"));
///     }
///
///     fn build_router() -> Router {
///         Router::new()
///             .get("/api/users", list_users)
///             .post("/api/users", create_user)
///     }
/// }
/// ```
pub trait Api {
    /// Idempotent table creation. Default is a no-op for APIs with no tables.
    fn init_tables() {}

    /// Build the request router. Called per request; cheap to construct.
    fn build_router() -> router::Router;

    /// Build the job router for this API. Default: empty (HTTP-only APIs).
    /// Apps that handle background jobs override this with
    /// `JobRouter::new().exact(...).prefix(...)`.
    fn build_job_router() -> JobRouter { JobRouter::new() }
}

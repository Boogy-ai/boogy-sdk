//! Boogy SDK — building blocks for hand-written and generated APIs.
//!
//! See [README](https://github.com/Boogy-ai/boogy-sdk/tree/main/crates/boogy-sdk)
//! for the full authoring guide. The short version:
//!
//! ```ignore
//! mod bindings {
//!     wit_bindgen::generate!({ world: "service", path: "../../boogy-wit/wit" });
//! }
//! boogy_sdk::wit_glue!(bindings, TodoApi);
//!
//! use boogy_sdk::Api;
//!
//! struct TodoApi;
//!
//! impl Api for TodoApi {
//!     fn init_tables() {
//!         create_table_from(&Table::new("todos").text("title").boolean("done"));
//!     }
//!     fn build_router() -> Router {
//!         Router::new().get("/api/todos", list_todos)
//!     }
//! }
//!
//! #[derive(Serialize)]
//! struct TodosCount { count: u64 }
//!
//! fn list_todos(_req: &mut Req<'_>) -> Result<Json<TodosCount>, ApiError> {
//!     let (_rows, total) = find_all_rows("todos")?;
//!     Ok(Json(TodosCount { count: total }))
//! }
//! ```
//!
//! # Modules
//!
//! - [`json`] — serde + serde_json re-exports
//! - [`response`] — HTTP response builders (ok, created, not_found, etc.)
//! - [`router`] — declarative request routing with path params
//! - [`rpc`] — JSON-RPC 2.0 envelope, error type, response helpers
//! - [`store`] — typed row accessors, table/query builders

